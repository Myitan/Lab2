char gameBoard[GAME_HEIGHT][GAME_WIDTH] = {0};
uint32_t Joystickx[2];
uint32_t Joysticky[2];
int currentTetromino = 0;
int currentX = GAME_WIDTH / 2 - 2;
int currentY = 0;
bool gameOver = false;
int score = 0;

int joystickXValue = 0;
int joystickYValue = 0;
bool joystickButton = false;

void initJoystick() {
	HAL_ADC_Start_DMA(&hadc1, Joystickx, 2);    // Start ADC for URX
	HAL_ADC_Start_DMA(&hadc2, Joysticky, 2);  // Start ADC for URY
}

void readJoystick() {
    joystickXValue = HAL_ADC_GetValue(&hadc1);

    joystickYValue = HAL_ADC_GetValue(&hadc2);

    joystickButton = (HAL_GPIO_ReadPin(JOYSTICK_SW_PORT, JOYSTICK_SW_PIN) == GPIO_PIN_RESET);
}

bool canMoveTetromino(int dx, int dy) {
    for (int y = 0; y < 4; y++) {
        for (int x = 0; x < 4; x++) {
            if (TETROMINO_SHAPES[currentTetromino][y][x]) {
                int newX = currentX + x + dx;
                int newY = currentY + y + dy;
                if (newX < 0 || newX >= GAME_WIDTH || newY >= GAME_HEIGHT || (newY >= 0 && gameBoard[newY][newX])) {
                    return false;
                }
            }
        }
    }
    return true;
}

void moveTetromino(int dx, int dy) {
    if (canMoveTetromino(dx, dy)) {
        currentX += dx;
        currentY += dy;
    }
}

void rotateTetromino() {
    char rotated[4][4] = {0};
    for (int y = 0; y < 4; y++) {
        for (int x = 0; x < 4; x++) {
            rotated[x][3 - y] = TETROMINO_SHAPES[currentTetromino][y][x];
        }
    }
    memcpy((void *)TETROMINO_SHAPES[currentTetromino], rotated, sizeof(rotated));
}

void lockTetromino() {
    for (int y = 0; y < 4; y++) {
        for (int x = 0; x < 4; x++) {
            if (TETROMINO_SHAPES[currentTetromino][y][x]) {
                gameBoard[currentY + y][currentX + x] = 1;
            }
        }
    }
    clearCompletedRows();
    spawnNewTetromino();
}

void clearCompletedRows() {
    for (int y = 0; y < GAME_HEIGHT; y++) {
        bool fullRow = true;
        for (int x = 0; x < GAME_WIDTH; x++) {
            if (!gameBoard[y][x]) {
                fullRow = false;
                break;
            }
        }
        if (fullRow) {
            for (int yy = y; yy > 0; yy--) {
                memcpy(gameBoard[yy], gameBoard[yy - 1], GAME_WIDTH);
            }
            memset(gameBoard[0], 0, GAME_WIDTH);
            score += 100;
        }
    }
}

void spawnNewTetromino() {
    currentTetromino = rand() % 7;
    currentX = GAME_WIDTH / 2 - 2;
    currentY = 0;
    if (!canMoveTetromino(0, 0)) {
        gameOver = true;
    }
}

void renderGame() {
    BSP_LCD_Clear(LCD_COLOR_BLACK);
    BSP_LCD_SetTextColor(LCD_COLOR_WHITE);

    // game board
    for (int y = 0; y < GAME_HEIGHT; y++) {
        for (int x = 0; x < GAME_WIDTH; x++) {
            if (gameBoard[y][x]) {
                BSP_LCD_FillRect(x * 10, y * 10, 10, 10);
            }
        }
    }
    // termino
    BSP_LCD_SetTextColor(LCD_COLOR_GREEN);
    for (int y = 0; y < 4; y++) {
        for (int x = 0; x < 4; x++) {
            if (TETROMINO_SHAPES[currentTetromino][y][x]) {
                int drawX = (currentX + x) * 10;
                int drawY = (currentY + y) * 10;
                BSP_LCD_FillRect(drawX, drawY, 10, 10);
            }
        }
    }
}


void showStartScreen() {
    BSP_LCD_Clear(LCD_COLOR_BLACK);
    BSP_LCD_SetTextColor(LCD_COLOR_WHITE);
    BSP_LCD_DisplayStringAt(0, 120, (uint8_t *)"Press Joystick to Start", CENTER_MODE);

    while (!joystickButton) {
        readJoystick();
        HAL_Delay(50);
    }
}

void showEndScreen() {
    BSP_LCD_Clear(LCD_COLOR_RED);
    BSP_LCD_SetTextColor(LCD_COLOR_WHITE);
    BSP_LCD_DisplayStringAt(0, 120, (uint8_t *)"Game Over", CENTER_MODE);
    BSP_LCD_DisplayStringAt(0, 140, (uint8_t *)"Press Joystick to Restart", CENTER_MODE);

    while (!joystickButton) {
        readJoystick();
        HAL_Delay(50);
    }

    memset(gameBoard, 0, sizeof(gameBoard));
    gameOver = false;
    score = 0;
}


void gameLoop() {
    showStartScreen();
    spawnNewTetromino();

    while (!gameOver) {
        readJoystick();

        if (joystickXValue > THRESHOLD_RIGHT) {
            moveTetromino(1, 0);
        } else if (joystickXValue < THRESHOLD_LEFT) {
            moveTetromino(-1, 0);
        }

        if (joystickYValue < THRESHOLD_DOWN) {
            moveTetromino(0, 1);
        }

        if (joystickButton) {
            rotateTetromino();
        }

        renderGame();

        HAL_Delay(500); // game speed
    }

    showEndScreen();
}


